# Choose a color for an alien and write an if-else chain

# Version that runs the if statement

alien_color = 'green'

if alien_color == 'green':
	print('Player has earned 5 points!')
else:
	print('Player has earned 10 points!')

# Version that doesn't run the if statement

alien_color = 'red'

if alien_color == 'green':
	print('Player has earned 5 points!')
else:
	print('Player has earned 10 points!')